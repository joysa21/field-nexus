const leafLookup = (value: TranslationValue, segments: string[]): string | undefined => {
  if (segments.length === 0) return typeof value === "string" ? value : undefined;
  if (typeof value === "string") return undefined;
  const [head, ...rest] = segments;
  const next = value[head];
  if (!next) return undefined;
  return leafLookup(next, rest);
};

export function translate(language: Language, key: string, params?: Record<string, string | number>): string {
  const fallback = leafLookup(translations.en, key.split(".")) ?? key;
  const localized = leafLookup(translations[language], key.split(".")) ?? fallback;
  if (!params) return localized;
  return localized.replace(/\{(\w+)\}/g, (_, param) => String(params[param] ?? ""));
}

